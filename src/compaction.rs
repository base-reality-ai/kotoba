/// Legacy: maybe compact if over threshold. Delegates to `full_compact`.
pub async fn maybe_compact(
    messages: &[serde_json::Value],
    client: &OllamaClient,
    verbose: bool,
) -> Result<Vec<serde_json::Value>> {
    if messages.len() <= 40 {
        return Ok(messages.to_vec());
    }
    // Passes `None` — sub-agent / legacy TUI callers don't thread a session
    // root today. Falls back to process cwd with a one-shot warning. Revisit
    // once those call sites carry a canonical session root.
    full_compact(messages, client, DEFAULT_KEEP_TAIL, verbose, 0, None).await
}

