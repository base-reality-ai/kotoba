//! Implements three-stage context trimming for LLM sessions.
//!
//! Handles token budget arithmetic, micro-compaction of tool results, and
//! macro-compaction to durable wiki knowledge.

use crate::ollama::client::OllamaClient;
use crate::util::safe_string_truncate;
use anyhow::Result;
use std::fmt::Write as _;
use std::path::{Path, PathBuf};

/// Latches true on the first compaction tee that had no threaded `project_root`
/// and fell back to `env::current_dir()`. The warning is emitted once per
/// process — enough to surface the coverage gap without spamming logs of
/// long-running daemons that repeat the fallback every compact.
static CWD_FALLBACK_WARNED: std::sync::atomic::AtomicBool =
    std::sync::atomic::AtomicBool::new(false);

fn warn_once_about_cwd_fallback() {
    use std::sync::atomic::Ordering;
    if !CWD_FALLBACK_WARNED.swap(true, Ordering::Relaxed) {
        crate::warnings::push_warning(
            "wiki: compact tee falling back to process cwd — caller did not \
             thread a session root. Synthesis may land in the wrong project \
             if cwd drifted. See cycle-9 Finding 2."
                .to_string(),
        );
    }
}

#[cfg(test)]
fn reset_cwd_fallback_warned() {
    CWD_FALLBACK_WARNED.store(false, std::sync::atomic::Ordering::Relaxed);
}

// ── Percentage-based thresholds (scale with any model's context window) ──────

/// Truncate content preserving head and tail lines.
/// For content with ≤3 lines, uses prefix truncation.
/// For multiline content, keeps ~60% head and ~40% tail with an omission marker.
fn smart_truncate(content: &str, max_chars: usize) -> String {
    if content.len() <= max_chars {
        return content.to_string();
    }

    let lines: Vec<&str> = content.lines().collect();
    if lines.len() <= 3 {
        return crate::util::safe_truncate(content, max_chars).to_string();
    }

    let head_budget = max_chars * 60 / 100;
    let tail_budget = max_chars - head_budget;

    let mut head_lines = Vec::new();
    let mut head_used = 0usize;
    for line in &lines {
        let cost = line.len() + 1; // +1 for newline
        if head_used + cost > head_budget && !head_lines.is_empty() {
            break;
        }
        head_lines.push(*line);
        head_used += cost;
    }

    let mut tail_lines = Vec::new();
    let mut tail_used = 0usize;
    for line in lines.iter().rev() {
        let cost = line.len() + 1;
        if tail_used + cost > tail_budget && !tail_lines.is_empty() {
            break;
        }
        tail_lines.push(*line);
        tail_used += cost;
    }
    tail_lines.reverse();

    let total_selected = head_lines.len() + tail_lines.len();
    if total_selected >= lines.len() {
        return content.to_string();
    }
    let omitted = lines.len() - total_selected;

    format!(
        "{}\n... ({} lines omitted) ...\n{}",
        head_lines.join("\n"),
        omitted,
        tail_lines.join("\n")
    )
}

/// Stage 1 (microcompact): trim tool results when context reaches this % of window.
const MICRO_COMPACT_PCT: f64 = 0.50;

/// Stage 2 (session memory compact): prune old messages at this % of window.
const SESSION_COMPACT_PCT: f64 = 0.65;

/// Stage 3 (full summarization compact): summarize conversation at this % of window.
const FULL_COMPACT_PCT: f64 = 0.80;

/// Headroom reserved for the summary-response tokens + per-message overhead.
/// The summary *input* must fit inside `(1 - HEADROOM) * context_window`, or
/// the summarization call itself will overflow the model window (Phase 1 Bug 2).
const COMPACT_HEADROOM_PCT: f64 = 0.15;

/// Rough token budget for the summary prompt template and response allowance,
/// subtracted from the window before computing the history char budget.
const SUMMARY_PROMPT_OVERHEAD_TOKENS: usize = 256;

/// Char budget for the project-summary slice injected into the compaction
/// prompt. Paired with the full page-catalog snippet — keeping the summary
/// bounded ensures a deep wiki doesn't crowd out the conversation history.
const COMPACT_PROJECT_SUMMARY_BUDGET_CHARS: usize = 2048;

/// Warn the user when context reaches this % of window.
const WARNING_PCT: f64 = 0.60;

/// Default number of recent messages to always preserve during compaction.
const DEFAULT_KEEP_TAIL: usize = 10;

/// Default max chars per tool result before truncation.
const DEFAULT_MAX_TOOL_CHARS: usize = 500;

/// Tool roles that are safe to trim (output can be regenerated by re-running tools).
const COMPACTABLE_ROLES: &[&str] = &["tool"];

/// Thresholds computed from a model's context window size.
#[derive(Debug, Clone)]
pub struct CompactionThresholds {
    /// Stage 1: microcompact triggers here (tokens).
    pub micro_compact: usize,
    /// Stage 2: session memory compact triggers here (tokens).
    pub session_compact: usize,
    /// Stage 3: full summarization triggers here (tokens).
    pub full_compact: usize,
    /// Warn the user at this token count.
    pub warning: usize,
    /// Number of recent messages to always preserve during compaction.
    pub keep_tail: usize,
    /// Max chars per tool result before truncation in microcompact.
    pub max_tool_chars: usize,
}

impl CompactionThresholds {
    /// Compute thresholds from a model's context window size using defaults.
    pub fn from_context_window(context_window: usize) -> Self {
        Self {
            micro_compact: (context_window as f64 * MICRO_COMPACT_PCT) as usize,
            session_compact: (context_window as f64 * SESSION_COMPACT_PCT) as usize,
            full_compact: (context_window as f64 * FULL_COMPACT_PCT) as usize,
            warning: (context_window as f64 * WARNING_PCT) as usize,
            keep_tail: DEFAULT_KEEP_TAIL,
            max_tool_chars: DEFAULT_MAX_TOOL_CHARS,
        }
    }

    /// Builder: override the number of tail messages to keep.
    // Public builder retained for external crate consumers and tests;
    // in-tree callers use `from_context_window` defaults.
    #[allow(dead_code)]
    pub fn with_keep_tail(mut self, keep_tail: usize) -> Self {
        self.keep_tail = keep_tail;
        self
    }

    /// Builder: override the max chars per tool result.
    // Public builder retained for external crate consumers and tests;
    // in-tree callers use `from_context_window` defaults.
    #[allow(dead_code)]
    pub fn with_max_tool_chars(mut self, max_tool_chars: usize) -> Self {
        self.max_tool_chars = max_tool_chars;
        self
    }
}

/// Estimate the token count of a message list.
///
/// Delegates to [`crate::tokens::conversation_tokens`] which counts content,
/// `tool_calls`, images, and per-message overhead.
pub fn estimate_tokens(messages: &[serde_json::Value]) -> usize {
    crate::tokens::conversation_tokens(messages)
}

// ── Stage 1: Microcompact — trim tool results ───────────────────────────────

/// Truncate verbose tool results to reduce context without losing conversation
/// structure. Each tool message over `max_chars` is trimmed. Stops early when
/// estimated tokens drop below `target_tokens`.
///
/// Returns `(chars_removed, messages_affected)`.
pub fn microcompact(
    messages: &mut [serde_json::Value],
    target_tokens: usize,
    max_tool_chars: usize,
) -> (usize, usize) {
    let mut chars_removed = 0usize;
    let mut messages_affected = 0usize;

    let mut total_chars: usize = messages
        .iter()
        .filter_map(|m| m["content"].as_str())
        .map(|s| s.len())
        .sum();

    for msg in messages.iter_mut() {
        if total_chars / 4 <= target_tokens {
            break;
        }

        let role = msg["role"].as_str().unwrap_or("");
        if !COMPACTABLE_ROLES.contains(&role) {
            continue;
        }

        // Preserve error messages — the model needs diagnostic details to fix issues
        if msg["is_error"].as_bool().unwrap_or(false) {
            continue;
        }

        if let Some(content) = msg["content"].as_str() {
            if content.len() <= max_tool_chars {
                continue;
            }
            // Skip already-truncated messages to prevent double-truncation
            if content.ends_with(" chars removed]") && content.contains("[truncated — ") {
                continue;
            }
            let truncated_body = smart_truncate(content, max_tool_chars);
            if truncated_body.len() >= content.len() {
                continue;
            }
            let removed = content.len() - truncated_body.len();
            let truncated = format!(
                "{}\n[truncated — {} chars removed]",
                truncated_body, removed
            );
            chars_removed += removed;
            total_chars -= removed;
            messages_affected += 1;
            msg["content"] = serde_json::Value::String(truncated);
        }
    }

    (chars_removed, messages_affected)
}

// ── Stage 2: Session memory compact — drop old messages ─────────────────────

/// Drop middle messages, keeping system prompt and the last `keep_tail` messages.
/// No API call needed — just a hard trim.
///
/// Returns the compacted message list.
pub fn session_memory_compact(
    messages: &[serde_json::Value],
    keep_tail: usize,
) -> Vec<serde_json::Value> {
    if messages.len() <= keep_tail + 1 {
        return messages.to_vec();
    }

    let mut result = vec![messages[0].clone()];
    let tail_start = messages.len().saturating_sub(keep_tail);
    let dropped_slice = &messages[1..tail_start];
    let inventory = summarize_dropped_messages(dropped_slice);
    result.push(serde_json::json!({
        "role": "system",
        "content": format!(
            "[Context compacted — {} earlier messages dropped to free space. {}Recent conversation preserved below.]",
            dropped_slice.len(),
            inventory
        )
    }));
    result.extend_from_slice(&messages[tail_start..]);
    result
}

const FILE_EXTENSIONS: &[&str] = &[
    ".rs", ".ts", ".js", ".py", ".md", ".toml", ".yaml", ".yml", ".json", ".html", ".css", ".sh",
    ".txt", ".cfg", ".lock",
];

fn extract_file_paths(messages: &[serde_json::Value], max_paths: usize) -> Vec<String> {
    let mut seen = std::collections::HashSet::new();
    let mut paths: Vec<String> = Vec::new();

    for msg in messages {
        if let Some(tcs) = msg["tool_calls"].as_array() {
            for tc in tcs {
                if let Some(args_str) = tc
                    .get("function")
                    .and_then(|f| f.get("arguments"))
                    .and_then(|a| a.as_str())
                {
                    if let Ok(args) = serde_json::from_str::<serde_json::Value>(args_str) {
                        for key in &["file_path", "path"] {
                            if let Some(p) = args.get(key).and_then(|v| v.as_str()) {
                                let s = p.to_string();
                                if seen.insert(s.clone()) && paths.len() < max_paths {
                                    paths.push(s);
                                }
                            }
                        }
                    }
                }
            }
        }

        if let Some(content) = msg["content"].as_str() {
            for word in content.split_whitespace() {
                let clean = word.trim_matches(|c: char| {
                    c == '\'' || c == '"' || c == '`' || c == ',' || c == ')' || c == '('
                });
                if clean.contains('.')
                    && FILE_EXTENSIONS.iter().any(|ext| clean.ends_with(ext))
                    && clean.len() > 3
                {
                    let s = clean.to_string();
                    if seen.insert(s.clone()) && paths.len() < max_paths {
                        paths.push(s);
                    }
                }
            }
        }
    }

    paths
}

fn summarize_dropped_messages(messages: &[serde_json::Value]) -> String {
    if messages.is_empty() {
        return String::new();
    }

    let mut user_count = 0usize;
    let mut assistant_count = 0usize;
    let mut tool_count = 0usize;
    let mut tools_seen = std::collections::HashSet::new();
    let mut tools: Vec<String> = Vec::new();

    for msg in messages {
        match msg["role"].as_str().unwrap_or("") {
            "user" => user_count += 1,
            "assistant" => {
                assistant_count += 1;
                if let Some(tcs) = msg["tool_calls"].as_array() {
                    for tc in tcs {
                        if let Some(name) = tc["function"]["name"].as_str() {
                            let s = name.to_string();
                            if tools_seen.insert(s.clone()) {
                                tools.push(s);
                            }
                        }
                    }
                }
            }
            "tool" => {
                tool_count += 1;
                if let Some(name) = msg["name"].as_str() {
                    let s = name.to_string();
                    if tools_seen.insert(s.clone()) {
                        tools.push(s);
                    }
                }
            }
            _ => {}
        }
    }

    let files = extract_file_paths(messages, 15);

    let mut parts = Vec::new();
    let mut role_parts = Vec::new();
    if tool_count > 0 {
        role_parts.push(format!("{} tool results", tool_count));
    }
    if assistant_count > 0 {
        role_parts.push(format!("{} assistant", assistant_count));
    }
    if user_count > 0 {
        role_parts.push(format!("{} user", user_count));
    }
    if !role_parts.is_empty() {
        parts.push(format!("({})", role_parts.join(", ")));
    }
    if !files.is_empty() {
        parts.push(format!("Files referenced: {}", files.join(", ")));
    }
    if !tools.is_empty() {
        parts.push(format!("Tools used: {}", tools.join(", ")));
    }

    let mut summary = parts.join(". ");
    if !summary.is_empty() {
        summary.push_str(". ");
    }
    if summary.len() > 500 {
        safe_string_truncate(&mut summary, 497);
        summary.push_str("...");
    }
    summary
}

// ── Stage 3: Full summarization compact ─────────────────────────────────────

/// Build the wiki-aware prefix injected ahead of `history_text` in the
/// compaction summary request. Combines the project-summary slice (dm's
/// highest-level comprehension of the project) with the page-catalog
/// snippet (the map of what exists in the wiki). Either half may be
/// absent; returns `""` when both are `None` so callers can concatenate
/// unconditionally.
///
/// Pure: all inputs are borrowed strings, no I/O. Kept here (not in
/// `wiki/mod.rs`) because the wrapping boilerplate is compaction-prompt-
/// specific — the wiki layer shouldn't know about summary prompts.
fn build_wiki_compact_prefix(project_summary: Option<&str>, page_catalog: Option<&str>) -> String {
    let mut out = String::new();
    if let Some(summary) = project_summary {
        let trimmed = summary.trim();
        if !trimmed.is_empty() {
            out.push_str("Project summary from the wiki (dm's accumulated understanding):\n\n");
            out.push_str(trimmed);
            out.push_str("\n\n---\n\n");
        }
    }
    if let Some(catalog) = page_catalog {
        let trimmed = catalog.trim();
        if !trimmed.is_empty() {
            out.push_str(
                "Known project entities from the wiki (use these names/paths verbatim when they appear):\n\n",
            );
            out.push_str(trimmed);
            out.push_str("\n\n---\n\n");
        }
    }
    out
}

/// Summarize the middle portion of the conversation using Ollama, preserving
/// the system prompt and the last `KEEP_TAIL` messages.
async fn full_compact(
    messages: &[serde_json::Value],
    client: &OllamaClient,
    keep_tail: usize,
    verbose: bool,
    context_window: usize,
    project_root: Option<&Path>,
) -> Result<Vec<serde_json::Value>> {
    if messages.is_empty() {
        return Ok(Vec::new());
    }
    let system = &messages[0];
    let tail_start = messages.len().saturating_sub(keep_tail);
    if tail_start <= 1 {
        return Ok(messages.to_vec());
    }
    let middle = &messages[1..tail_start];
    let tail = &messages[tail_start..];

    if middle.is_empty() {
        return Ok(messages.to_vec());
    }

    // ── Wiki handle + context snippet (read side of the compact bridge) ──
    // Hoisted above history build so the snippet length counts against the
    // pre-trim budget (Phase 1 Bug 2 invariant: summary call must not
    // overflow its own window). The handle is also reused by the synthesis
    // write below, so the wiki is opened at most once per compact.
    let wiki_root: Option<PathBuf> = match project_root {
        Some(p) => Some(p.to_path_buf()),
        None => {
            // Lock-gated cwd read FIRST, then the warn-once push — so a
            // concurrent caller blocked on `CWD_LOCK` can't push the
            // fallback warning into a tee test's drain window. Cycle 24
            // surfaced this ordering trap during the tests-2+3 un-ignore.
            // See memory `project_compaction_tee_ignored_race`.
            let cwd = crate::tools::read_cwd_synchronized();
            warn_once_about_cwd_fallback();
            cwd
        }
    };
    let wiki_handle: Option<crate::wiki::Wiki> = match wiki_root.as_deref() {
        Some(r) => match crate::wiki::Wiki::open(r) {
            Ok(w) => Some(w),
            Err(e) => {
                crate::warnings::push_warning(format!(
                    "wiki: compact context/synthesis skipped — open failed: {}",
                    e
                ));
                None
            }
        },
        None => None,
    };
    let wiki_snippet: Option<String> = wiki_handle.as_ref().and_then(|w| w.context_snippet());
    let project_summary_snippet: Option<String> = wiki_handle
        .as_ref()
        .and_then(|w| w.project_summary_snippet(COMPACT_PROJECT_SUMMARY_BUDGET_CHARS));
    if verbose {
        if let Some(s) = wiki_snippet.as_ref() {
            crate::warnings::push_warning(format!(
                "wiki: context_snippet attached ({} chars)",
                s.len()
            ));
        }
        if let Some(s) = project_summary_snippet.as_ref() {
            crate::warnings::push_warning(format!(
                "wiki: project_summary_snippet attached ({} chars)",
                s.len()
            ));
        }
    }

    if verbose {
        crate::warnings::push_warning(format!(
            "Full compact: {} messages → summarizing {} middle messages",
            messages.len(),
            middle.len()
        ));
    }

    let history_text = middle
        .iter()
        .map(|m| {
            let role = m["role"].as_str().unwrap_or("unknown");
            let content = m["content"].as_str().unwrap_or("[tool call]");
            let truncated = crate::util::safe_truncate(content, 500);
            format!("[{}]: {}", role, truncated)
        })
        .collect::<Vec<_>>()
        .join("\n");

    // Pre-trim to leave summary-response + prompt-template headroom inside the
    // model window. Without this guard the summary call itself can overflow
    // when FULL_COMPACT_PCT fires at 80% and the middle is already large.
    // context_window==0 preserves the legacy `maybe_compact` contract where
    // callers don't thread a window size. Both wiki halves (catalog +
    // project summary) cost budget since they ride ahead of history_text
    // in the prompt below — sum their bytes before truncation so total
    // payload stays within window.
    let snippet_chars = wiki_snippet.as_ref().map_or(0, |s| s.len())
        + project_summary_snippet.as_ref().map_or(0, |s| s.len());
    let history_text = if context_window > 0 {
        let headroom = (context_window as f64 * COMPACT_HEADROOM_PCT) as usize;
        let budget_tokens = context_window
            .saturating_sub(headroom)
            .saturating_sub(SUMMARY_PROMPT_OVERHEAD_TOKENS);
        let budget_chars = budget_tokens
            .saturating_mul(4)
            .saturating_sub(snippet_chars);
        if history_text.len() > budget_chars {
            if verbose {
                crate::warnings::push_warning(format!(
                    "Full compact pre-trim: history {} chars → {} chars (window {}, headroom {}%, wiki snippet {} chars)",
                    history_text.len(),
                    budget_chars,
                    context_window,
                    (COMPACT_HEADROOM_PCT * 100.0) as usize,
                    snippet_chars,
                ));
            }
            smart_truncate(&history_text, budget_chars)
        } else {
            history_text
        }
    } else {
        history_text
    };

    let wiki_prefix =
        build_wiki_compact_prefix(project_summary_snippet.as_deref(), wiki_snippet.as_deref());

    let summary_request = vec![serde_json::json!({
        "role": "user",
        "content": format!(
            "Summarize the following conversation history. Preserve:\n\
             1. Primary user request and goals\n\
             2. Key decisions made\n\
             3. Files created or modified (with paths)\n\
             4. Errors encountered and how they were resolved\n\
             5. Current state of work\n\
             6. Pending tasks or next steps\n\
             Be concise but complete.\n\n{}{}",
            wiki_prefix,
            history_text
        )
    })];

    let response = client.chat(&summary_request, &[]).await?;

    // ── Compact-to-wiki bridge (write side) ─────────────────────────────
    // Persist the summary before messages are dropped. Best-effort; a
    // failing wiki write must not fail the compact itself. Reuses the
    // handle opened above; when `wiki_handle` is None, open already
    // emitted a warning, so we skip silently here.
    if let Some(wiki) = wiki_handle.as_ref() {
        let wiki_sources = extract_file_paths(middle, 20);
        if let Err(e) =
            wiki.write_compact_synthesis(&response.message.content, middle.len(), &wiki_sources)
        {
            crate::warnings::push_warning(format!(
                "wiki: compact synthesis write failed ({} middle msgs): {}",
                middle.len(),
                e
            ));
        }
    }

    let mut compacted = vec![system.clone()];
    compacted.push(serde_json::json!({
        "role": "system",
        "content": format!(
            "## Previous conversation summary\n\n{}", response.message.content
        )
    }));

    let file_paths = extract_file_paths(middle, 5);
    if !file_paths.is_empty() && context_window > 0 {
        let budget_tokens = context_window / 5;
        let mut file_sections = Vec::new();
        let mut total_chars = 0usize;

        for path in &file_paths {
            let p = std::path::Path::new(path);
            if !p.exists() {
                continue;
            }
            if let Ok(content) = std::fs::read_to_string(p) {
                let truncated: String = content.lines().take(500).collect::<Vec<_>>().join("\n");
                let chars = truncated.len();
                if (total_chars + chars) / 4 > budget_tokens {
                    break;
                }
                file_sections.push(format!("### {}\n```\n{}\n```", path, truncated));
                total_chars += chars;
            }
        }

        if !file_sections.is_empty() {
            if verbose {
                crate::warnings::push_warning(format!(
                    "Restored {} files after compaction ({} chars)",
                    file_sections.len(),
                    total_chars
                ));
            }
            compacted.push(serde_json::json!({
                "role": "system",
                "content": format!(
                    "## Recently-touched files (restored after compaction)\n\n{}",
                    file_sections.join("\n\n")
                )
            }));
        }
    }

    compacted.extend_from_slice(tail);

    Ok(compacted)
}

// ── Emergency-compact wiki preservation (Phase 1 Bug 5) ─────────────────────

/// Max total chars for the raw digest dumped into the wiki on emergency
/// compact. Keeps pathological multi-MB histories from bloating the
/// synthesis folder.
const EMERGENCY_DIGEST_CAP_CHARS: usize = 16 * 1024;

/// Per-message char cap inside the emergency digest. Mirrors the 500-char
/// convention used by `full_compact`'s `history_text` builder, slightly tighter
/// so more messages fit under `EMERGENCY_DIGEST_CAP_CHARS`.
const EMERGENCY_PER_MESSAGE_CHARS: usize = 400;

/// Best-effort wiki synthesis written BEFORE the emergency drop. Uses raw
/// message text (no Ollama call) — we reach this path specifically because
/// Stage 3 summarization failed or the circuit breaker tripped, so the
/// model is off-limits. Preserves what `session_memory_compact` is about
/// to destroy so the wiki retains what the conversation cannot.
///
/// Never propagates: failures push warnings and return.
fn write_emergency_synthesis(
    messages: &[serde_json::Value],
    thresholds: &CompactionThresholds,
    project_root: Option<&Path>,
) {
    if messages.len() <= thresholds.keep_tail + 1 {
        return;
    }
    let tail_start = messages.len().saturating_sub(thresholds.keep_tail);
    let dropped = &messages[1..tail_start];
    if dropped.is_empty() {
        return;
    }

    let mut digest = String::new();
    let mut truncated_notice = false;
    for msg in dropped {
        if digest.len() >= EMERGENCY_DIGEST_CAP_CHARS {
            truncated_notice = true;
            break;
        }
        let role = msg["role"].as_str().unwrap_or("unknown");
        let content = msg["content"].as_str().unwrap_or("[tool call]");
        let truncated = crate::util::safe_truncate(content, EMERGENCY_PER_MESSAGE_CHARS);
        writeln!(digest, "[{}]: {}", role, truncated).expect("write to String never fails");
    }
    if truncated_notice {
        digest.push_str("\n... (emergency digest truncated) ...\n");
    }

    let sources = extract_file_paths(dropped, 20);

    let wiki_root: Option<PathBuf> = match project_root {
        Some(p) => Some(p.to_path_buf()),
        None => {
            // Same lock-then-warn ordering as `full_compact`'s None
            // arm — see cycle 24 fix in
            // `project_compaction_tee_ignored_race`.
            let cwd = crate::tools::read_cwd_synchronized();
            warn_once_about_cwd_fallback();
            cwd
        }
    };
    let Some(root) = wiki_root else { return };

    match crate::wiki::Wiki::open(&root) {
        Ok(wiki) => {
            let body = format!(
                "**Emergency compact** — Stage 3 summarization failed or \
                 the circuit breaker tripped. Raw message digest preserved \
                 below (no LLM summary available).\n\n```\n{}```",
                digest
            );
            if let Err(e) = wiki.write_compact_synthesis(&body, dropped.len(), &sources) {
                crate::warnings::push_warning(format!(
                    "wiki: emergency synthesis write failed ({} dropped msgs): {}",
                    dropped.len(),
                    e
                ));
            }
        }
        Err(e) => {
            crate::warnings::push_warning(format!(
                "wiki: emergency synthesis skipped — open failed: {}",
                e
            ));
        }
    }
}

// ── Main compaction pipeline ────────────────────────────────────────────────

/// Result of running the compaction pipeline.
pub struct CompactionResult {
    /// Messages to send to the model (may be compacted).
    pub messages: Vec<serde_json::Value>,
    /// What stage was applied (if any).
    pub stage: CompactionStage,
    /// Whether the session messages should be replaced with these.
    pub replace_session: bool,
}

#[derive(Debug, Clone, PartialEq)]
pub enum CompactionStage {
    /// No compaction needed.
    None,
    /// Stage 1: tool results trimmed.
    Microcompact {
        chars_removed: usize,
        messages_affected: usize,
    },
    /// Stage 2: old messages dropped.
    SessionMemory { messages_dropped: usize },
    /// Stage 3: conversation summarized via model.
    FullSummary { messages_summarized: usize },
    /// Emergency: force-dropped to last N messages.
    Emergency,
}

/// Maximum consecutive Stage 3 (full summarization) failures before the circuit
/// breaker trips and skips straight to emergency compact. Prevents wasting API
/// calls when the model is unreachable or the context is irrecoverably over limit.
const MAX_COMPACT_FAILURES: usize = 3;

// ── Context-overflow recovery (Phase 1 Bug 1) ───────────────────────────────

/// Detect whether an error from `chat_stream_with_tools` / `chat` signals that
/// the model rejected the request for exceeding its context window. Both the
/// CLI (`conversation.rs`) and TUI (`tui/agent/turn.rs`) streaming error
/// handlers route through this so overflow handling cannot drift between call
/// sites.
pub fn is_context_overflow(err: &anyhow::Error) -> bool {
    let msg = format!("{:#}", err).to_lowercase();
    msg.contains("context length")
        || msg.contains("too long")
        || msg.contains("maximum context")
        || msg.contains("context_length")
}

/// Hard-trim messages on an observed context-overflow error, emit a visible
/// warning (so headless callers see the event), and return the compacted vec
/// ready to retry the send.
///
/// Uses `session_memory_compact` — a deterministic, no-Ollama-call trim.
/// Intentionally does NOT invoke the full pipeline: we're on the overflow
/// fallback path precisely because the model path is unreliable, so calling
/// Stage 3 (which needs Ollama) would risk cascade failure.
pub fn compact_for_overflow_retry(
    messages: &[serde_json::Value],
    keep_tail: usize,
) -> Vec<serde_json::Value> {
    let before = messages.len();
    let compacted = session_memory_compact(messages, keep_tail);
    crate::warnings::push_warning(format!(
        "Context overflow — hard-compacted {} → {} messages, retrying send",
        before,
        compacted.len()
    ));
    compacted
}

/// Run the 3-stage compaction pipeline. Each stage only triggers if the previous
/// stage didn't bring context below the threshold.
///
/// `compact_failures` tracks consecutive Stage 3 failures across rounds. When it
/// reaches `MAX_COMPACT_FAILURES`, Stage 3 is skipped (circuit breaker). A
/// successful Stage 3 resets the counter to 0.
///
/// All thresholds are percentage-based and scale with the model's context window.
///
/// Test-only wrapper that elides the circuit-breaker counter. Production callers
/// (conversation.rs, tui/agent/turn.rs) thread `session.compact_failures` directly
/// into [`compact_pipeline_with_failures`] so the counter survives across turns.
#[cfg(test)]
pub async fn compact_pipeline(
    messages: &mut Vec<serde_json::Value>,
    client: &OllamaClient,
    thresholds: &CompactionThresholds,
    verbose: bool,
    project_root: Option<&Path>,
) -> CompactionResult {
    compact_pipeline_with_failures(messages, client, thresholds, verbose, &mut 0, project_root)
        .await
}

/// Like `compact_pipeline` but accepts a mutable failure counter for circuit
/// breaker tracking across rounds.
pub async fn compact_pipeline_with_failures(
    messages: &mut Vec<serde_json::Value>,
    client: &OllamaClient,
    thresholds: &CompactionThresholds,
    verbose: bool,
    compact_failures: &mut usize,
    project_root: Option<&Path>,
) -> CompactionResult {
    let current_tokens = estimate_tokens(messages);

    // Under all thresholds — no compaction needed
    if current_tokens < thresholds.micro_compact {
        return CompactionResult {
            messages: Vec::new(),
            stage: CompactionStage::None,
            replace_session: false,
        };
    }

    // ── Stage 1: Microcompact (trim tool results) ───────────────────────────
    if current_tokens >= thresholds.micro_compact {
        let target = thresholds.micro_compact * 90 / 100; // aim for 90% of trigger
        let (chars_removed, msgs_affected) =
            microcompact(messages, target, thresholds.max_tool_chars);
        if chars_removed > 0 {
            if verbose {
                crate::warnings::push_warning(format!(
                    "Stage 1 (microcompact): trimmed {} chars from {} tool results",
                    chars_removed, msgs_affected
                ));
            }
            let after_tokens = estimate_tokens(messages);
            if after_tokens < thresholds.session_compact {
                return CompactionResult {
                    messages: messages.clone(),
                    stage: CompactionStage::Microcompact {
                        chars_removed,
                        messages_affected: msgs_affected,
                    },
                    replace_session: true,
                };
            }
        }
    }

    // ── Stage 2: Session memory compact (drop old messages) ─────────────────
    if estimate_tokens(messages) >= thresholds.session_compact {
        let before = messages.len();
        let compacted = session_memory_compact(messages, thresholds.keep_tail);
        let dropped = before.saturating_sub(compacted.len());

        if verbose {
            crate::warnings::push_warning(format!(
                "Stage 2 (session memory): dropped {} messages, {} → {}",
                dropped,
                before,
                compacted.len()
            ));
        }

        let after_tokens = estimate_tokens(&compacted);
        if after_tokens < thresholds.full_compact {
            return CompactionResult {
                messages: compacted,
                stage: CompactionStage::SessionMemory {
                    messages_dropped: dropped,
                },
                replace_session: true,
            };
        }

        // Stage 2 wasn't enough — update messages for stage 3
        *messages = compacted;
    }

    // ── Stage 3: Full summarization compact ─────────────────────────────────
    if estimate_tokens(messages) >= thresholds.full_compact {
        if *compact_failures >= MAX_COMPACT_FAILURES {
            crate::warnings::push_warning(format!(
                "Stage 3 skipped: {} consecutive failures (circuit breaker)",
                compact_failures
            ));
        } else {
            let middle_count = messages.len().saturating_sub(thresholds.keep_tail + 1);
            if verbose {
                crate::warnings::push_warning(format!(
                    "Stage 3 (full compact): summarizing {} middle messages",
                    middle_count
                ));
            } else {
                crate::warnings::push_warning("Compacting context…".to_string());
            }

            let cw = (thresholds.full_compact as f64 / FULL_COMPACT_PCT) as usize;
            match full_compact(
                messages,
                client,
                thresholds.keep_tail,
                verbose,
                cw,
                project_root,
            )
            .await
            {
                Ok(compacted) => {
                    *compact_failures = 0;
                    return CompactionResult {
                        messages: compacted,
                        stage: CompactionStage::FullSummary {
                            messages_summarized: middle_count,
                        },
                        replace_session: true,
                    };
                }
                Err(e) => {
                    *compact_failures += 1;
                    crate::warnings::push_warning(format!(
                        "Stage 3 failed ({}/{} consecutive): {} — falling back to emergency compact",
                        compact_failures, MAX_COMPACT_FAILURES, e
                    ));
                }
            }
        }
    }

    // ── Emergency: preserve what we can in the wiki, then force-drop ────────
    // `write_emergency_synthesis` dumps a raw-text digest of the to-be-dropped
    // middle messages into the wiki synthesis folder. No LLM call — we're here
    // because Stage 3 failed or the circuit breaker tripped, so the model is
    // off-limits. Best-effort: any failure pushes a warning and returns.
    write_emergency_synthesis(messages, thresholds, project_root);
    let compacted = session_memory_compact(messages, thresholds.keep_tail);
    CompactionResult {
        messages: compacted,
        stage: CompactionStage::Emergency,
        replace_session: true,
    }
}

