    // Phase 1 Bug 2 guard under the new accounting: snippet bytes are
    // subtracted from the pre-trim budget so the summary call's payload
    // (wiki prefix + history) stays inside the window. Proves that:
    //   1. `Full compact pre-trim` warning reports the wiki snippet size.
    //   2. post-trim history length + snippet length stays under
    //      `context_window * (1 - HEADROOM) * 4` (with prompt overhead).
    #[tokio::test]
    #[allow(clippy::await_holding_lock)]
    async fn full_compact_pretrim_budget_accounts_for_snippet() {
        let project_tmp = tempfile::TempDir::new().unwrap();
        let project_root = project_tmp.path().to_path_buf();

        // Seed a wiki with enough entries to produce a non-trivial snippet.
        let wiki = crate::wiki::Wiki::open(&project_root).unwrap();
        let entries: Vec<crate::wiki::IndexEntry> = (0..40)
            .map(|i| crate::wiki::IndexEntry {
                title: format!("Entity{:02}", i),
                path: format!("entities/entity_{:02}.md", i),
                one_liner: format!("Entity {} is a test entry with enough bytes to matter.", i),
                category: crate::wiki::PageType::Entity,
                last_updated: None,
                outcome: None,
            })
            .collect();
        wiki.save_index(&crate::wiki::WikiIndex { entries })
            .unwrap();

        let mut server = mockito::Server::new_async().await;
        let _mock = server
            .mock("POST", "/chat")
            .with_status(200)
            .with_header("content-type", "application/json")
            .with_body(
                r#"{"message":{"role":"assistant","content":"budget test summary"},"prompt_eval_count":0,"eval_count":0,"eval_duration":0}"#,
            )
            .create_async()
            .await;

        let client = crate::ollama::client::OllamaClient::new(server.url(), "m".to_string());

        let _warnings_guard = crate::warnings::WARNINGS_LOCK
            .lock()
            .unwrap_or_else(|e| e.into_inner());
        // Clear stale warnings so the assertion can find ours deterministically.
        let _ = crate::warnings::drain_warnings();

        // Window sized so pre-trim must fire on this message volume.
        let context_window: usize = 10_000;
        let msgs = build_messages(200, 400, "user");
        full_compact(&msgs, &client, 5, true, context_window, Some(&project_root))
            .await
            .expect("full_compact should succeed under pre-trim");

        let warnings = crate::warnings::drain_warnings();
        let pretrim: Vec<&String> = warnings
            .iter()
            .filter(|w| w.contains("Full compact pre-trim"))
            .collect();
        assert_eq!(
            pretrim.len(),
            1,
            "expected exactly one pre-trim warning; got: {:?}",
            warnings
        );
        let line = pretrim[0];

        // Warning must disclose the wiki snippet accounting — otherwise
        // the subtraction is in the code but the operator can't see it.
        assert!(
            line.contains("wiki snippet") && !line.contains("wiki snippet 0 chars"),
            "pre-trim warning must report non-zero wiki snippet size; got: {}",
            line
        );

        // Parse the reported post-trim history size and verify it plus
        // the snippet fits within `context_window * (1 - HEADROOM) * 4`.
        let after_arrow = line.split('→').nth(1).expect("pre-trim fmt has →");
        let post_trim_chars: usize = after_arrow
            .split_whitespace()
            .next()
            .expect("number after arrow")
            .parse()
            .expect("post-trim chars parse");

        let snippet_chars = wiki.context_snippet().map_or(0, |s| s.len());
        let headroom = (context_window as f64 * COMPACT_HEADROOM_PCT) as usize;
        let budget_cap = (context_window - headroom).saturating_mul(4);
        assert!(
            post_trim_chars + snippet_chars <= budget_cap,
            "history {} + snippet {} = {} must stay ≤ (window - headroom)*4 = {}; got pre-trim line: {}",
            post_trim_chars,
            snippet_chars,
            post_trim_chars + snippet_chars,
            budget_cap,
            line,
        );
    }

