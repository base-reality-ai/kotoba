    /// 60/40 split yields `head_budget == 0` (which happens at very small
    /// `max_chars`, e.g. 1, where `1 * 60 / 100 = 0`). The guard
    /// `head_used + cost > head_budget && !head_lines.is_empty()` must
    /// still admit the first line (because `head_lines.is_empty()` short-
    /// circuits the size check). Same on the tail side.
    ///
    /// This is the place a future "tighten the budget — drop the
    /// at-least-one fallback" refactor would silently:
    ///   - lose all content (return empty)
    ///   - panic on integer underflow
    ///   - return only the omission marker with no head/tail anchor lines
    #[test]
    fn smart_truncate_zero_head_budget_still_emits_first_and_last_via_fallback() {
        let lines: Vec<String> = (0..10).map(|i| format!("line-{}", i)).collect();
        let content = lines.join("\n");
        // max_chars=1 → head_budget = 1*60/100 = 0, tail_budget = 1 - 0 = 1.
        // Both buckets start with empty Vec, so the first-iteration push
        // through the !is_empty short-circuit must still happen.
        let result = smart_truncate(&content, 1);

        assert!(
            result.contains("line-0"),
            "at-least-one fallback must keep first line even with head_budget=0; got: {}",
            result
        );
        assert!(
            result.contains("line-9"),
            "at-least-one fallback must keep last line even with tail_budget=1; got: {}",
            result
        );
        assert!(
            result.contains("lines omitted"),
            "omission marker required when content is truncated; got: {}",
            result
        );
        // Sanity: no panic and a non-empty result.
        assert!(!result.is_empty());
    }

