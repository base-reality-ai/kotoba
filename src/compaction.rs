    #[tokio::test]
    #[allow(clippy::await_holding_lock)]
    async fn full_compact_pretrim_budget_accounts_for_both_snippet_halves() {
        // When both halves are present the pre-trim must subtract the *sum*
        // from the history budget — not just the catalog snippet. Asserts
        // post_trim_history + catalog_chars + summary_chars ≤ cap.
        let project_tmp = tempfile::TempDir::new().unwrap();
        let project_root = project_tmp.path().to_path_buf();

        let wiki = crate::wiki::Wiki::open(&project_root).unwrap();
        // Seed several entity pages so the summary is non-trivial.
        for i in 0..12 {
            let page = crate::wiki::WikiPage {
                title: format!("e{:02}", i),
                page_type: crate::wiki::PageType::Entity,
                layer: crate::wiki::Layer::Kernel,
                sources: vec![format!("src/e{:02}.rs", i)],
                last_updated: "2026-04-18 12:00:00".to_string(),
                entity_kind: Some(crate::wiki::EntityKind::Function),
                purpose: Some(format!("purpose text for entity {:02}", i)),
                key_exports: vec![],
                dependencies: vec![],
                outcome: None,
                scope: vec![],
                body: format!("# e{:02}\n", i),
                extras: ::std::collections::BTreeMap::new(),
            };
            wiki.write_page(&format!("entities/e{:02}.md", i), &page)
                .unwrap();
        }
        let entries: Vec<_> = (0..12)
            .map(|i| crate::wiki::IndexEntry {
                title: format!("e{:02}", i),
                path: format!("entities/e{:02}.md", i),
                one_liner: format!("entity {:02} one-liner with enough bytes", i),
                category: crate::wiki::PageType::Entity,
                last_updated: None,
                outcome: None,
            })
            .collect();
        wiki.save_index(&crate::wiki::WikiIndex { entries })
            .unwrap();
        wiki.write_project_summary().unwrap();

        let mut server = mockito::Server::new_async().await;
        let _mock = server
            .mock("POST", "/chat")
            .with_status(200)
            .with_header("content-type", "application/json")
            .with_body(
                r#"{"message":{"role":"assistant","content":"both halves summary"},"prompt_eval_count":0,"eval_count":0,"eval_duration":0}"#,
            )
            .create_async()
            .await;
        let client = crate::ollama::client::OllamaClient::new(server.url(), "m".to_string());

        let _warnings_guard = crate::warnings::WARNINGS_LOCK
            .lock()
            .unwrap_or_else(|e| e.into_inner());
        let _ = crate::warnings::drain_warnings();

        let context_window: usize = 10_000;
        let msgs = build_messages(200, 400, "user");
        full_compact(&msgs, &client, 5, true, context_window, Some(&project_root))
            .await
            .expect("full_compact succeeds under pre-trim with both halves");

        let warnings = crate::warnings::drain_warnings();
        let pretrim: Vec<&String> = warnings
            .iter()
            .filter(|w| w.contains("Full compact pre-trim"))
            .collect();
        assert_eq!(
            pretrim.len(),
            1,
            "one pre-trim warning; got: {:?}",
            warnings
        );
        let line = pretrim[0];
        let after_arrow = line.split('→').nth(1).expect("arrow present");
        let post_trim_chars: usize = after_arrow
            .split_whitespace()
            .next()
            .expect("number after arrow")
            .parse()
            .expect("post-trim chars parse");

        let catalog_chars = wiki.context_snippet().map_or(0, |s| s.len());
        let summary_chars = wiki
            .project_summary_snippet(COMPACT_PROJECT_SUMMARY_BUDGET_CHARS)
            .map_or(0, |s| s.len());
        assert!(
            summary_chars > 0,
            "fixture should produce a non-empty project summary"
        );
        let headroom = (context_window as f64 * COMPACT_HEADROOM_PCT) as usize;
        let budget_cap = (context_window - headroom).saturating_mul(4);
        assert!(
            post_trim_chars + catalog_chars + summary_chars <= budget_cap,
            "history {} + catalog {} + summary {} = {} must stay ≤ cap {}; line: {}",
            post_trim_chars,
            catalog_chars,
            summary_chars,
            post_trim_chars + catalog_chars + summary_chars,
            budget_cap,
            line,
        );
    }
