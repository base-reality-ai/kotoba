    // Four tests sharing EMERGENCY_LOCK so writes to the process-global
    // `DM_WIKI_AUTO_INGEST` env var don't race each other. Cross-module
    // races with `wiki::tests::ENV_LOCK` are tolerated — the same pattern
    // as the existing full_compact wiki tee tests (which don't set the
    // env var at all). Reusing CWD_LOCK would be wrong: these tests do
    // not mutate cwd, and CWD_LOCK is about cwd drift, not env ingest.
    static EMERGENCY_LOCK: std::sync::Mutex<()> = std::sync::Mutex::new(());

    struct EmergencyEnvGuard {
        prev: Option<String>,
    }
    impl EmergencyEnvGuard {
        fn enable() -> Self {
            let prev = std::env::var("DM_WIKI_AUTO_INGEST").ok();
            std::env::set_var("DM_WIKI_AUTO_INGEST", "1");
            Self { prev }
        }
    }
    impl Drop for EmergencyEnvGuard {
        fn drop(&mut self) {
            match &self.prev {
                Some(v) => std::env::set_var("DM_WIKI_AUTO_INGEST", v),
                None => std::env::remove_var("DM_WIKI_AUTO_INGEST"),
            }
        }
    }

